function dwell_time = manual_dwell_wrapper(dwell,delay)
% processes through dwell, depends on manual_dwell_count

path = 'D:\Dropbox\labwork\behavior\heatseeking-pilot\';
orl = {'20180627_2',...
       '20180628_2',...
       '20180629_3',...
       '20180711_1',...
       '20180712_1',...
       '20180713_2'};
orlgr3 = {'20180627_3',...
       '20180628_1',...
       '20180629_1',...
       '20180711_2',...
       '20180712_2',...
       '20180713_1'};
gr3 = {'20180627_1',...
       '20180628_3',...
       '20180629_2',...
       '20180711_3',...
       '20180712_3',...
       '20180713_3'};

dir_names = unique({dwell.dir_name});
dwell_time = struct('dir_name',dir_names,'time',[],'landing_n',[],'takeoff_n',[],'genotype',[]);

for i=1:length(dir_names)
    a = dwell(strcmp({dwell.dir_name},dir_names(i)));
    [dwell_time(i).time,dwell_time(i).landing_n,dwell_time(i).takeoff_n,~] = manual_dwell_count(a,path,delay);
    if any(strcmp(dir_names(i),orl))
        dwell_time(i).genotype = 'orl';
    elseif any(strcmp(dir_names(i),orlgr3))
        dwell_time(i).genotype = 'orlgr3';
    elseif any(strcmp(dir_names(i),gr3))
        dwell_time(i).genotype = 'gr3';
    end        
end



end