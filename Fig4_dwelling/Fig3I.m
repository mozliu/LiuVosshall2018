%% heatmaps of dwell location
orl = {'20180627_2',...
       '20180628_2',...
       '20180629_3',...
       '20180711_1',...
       '20180712_1',...
       '20180713_2'};
orlgr3 = {'20180627_3',...
       '20180628_1',...
       '20180629_1',...
       '20180711_2',...
       '20180712_2',...
       '20180713_1'};
gr3 = {'20180627_1',...
       '20180628_3',...
       '20180629_2',...
       '20180711_3',...
       '20180712_3',...
       '20180713_3'};
for i=1:length(dwell)
    if any(strcmp(dwell(i).dir_name,orl))
        dwell(i).genotype = 'orl';
    elseif any(strcmp(dwell(i).dir_name,orlgr3))
        dwell(i).genotype = 'orlgr3';
    elseif any(strcmp(dwell(i).dir_name,gr3))
        dwell(i).genotype = 'gr3';
    end
end
for i={'orl','orlgr3','gr3'}
    pos_l = vertcat(dwell(strcmp(i,{dwell.genotype})).landing);
    pos_t = vertcat(dwell(strcmp(i,{dwell.genotype})).takeoff);
    peak_l = heatseeking_heatmap(pos_l,[],[],1,36);
    title([i ' landings']);
    peak_t = heatseeking_heatmap(pos_t,[],[],1,36);
    title([i 'takeoffs']);
end